package uk.co.davidbaxter.letmepass.presentation;

/**
 * Modes of displaying password entries in the main view
 */
public enum DisplayMode {

    /**
     * Display passwords in the same hierarchical fashion as they are stored
     */
    EXPLORE,

    /**
     * Display every password (no folders)
     */
    ALL_PASSWORDS,

    /**
     * Display all favourite passwords
     */
    FAVORITES

}
